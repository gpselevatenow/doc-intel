def lookup(state: str, statute: str) -> str | None:
    """
    Return description for a state statute/citation code.
    statute may include or omit the state prefix (e.g. "CVC 22350" or "22350").

    Returns the description string or None if not found.
    """
    state_upper = state.upper()
    table = _STATUTES.get(state_upper)
    if not table:
        return None

    # Strip common prefixes (CVC, TTC, VTL, FS, PA, etc.)
    clean = statute.strip()
    clean = clean.replace(",", "").strip()
    import re
    clean = re.sub(
        r'^(?:CVC|TTC|VTL|FS|VEH\.?\s*CODE|TRANS\.?\s*CODE|PENAL|RSA|RCW|ORS|MCL|CGS|LSA|UCA|MCA|NRS|ARS|CRS|SDCL|WVC)\s*',
        '', clean, flags=re.IGNORECASE
    ).strip()

    return table.get(clean) or table.get(clean.lstrip("0"))


def all_statutes(state: str) -> dict[str, str]:
    """Return full statute table for a state, or empty dict."""
    return dict(_STATUTES.get(state.upper(), {}))
